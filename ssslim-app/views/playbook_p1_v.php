    <h1>PART FOUR:<br />
        Manage</h1>
    
    <h2 class="black">next-generation applications and new custom builds alongside your legacy and migrated environments</h2>
    
    <img src="<?= base_url() ?>images/playbooks/rectangle-head.jpg" class="rectangle" />
    
    <blockquote>
        <p>Most organizations today have structures where business, development, operations, and
            security are all in different organizations. With cloud computing, the approach is that of
            a cross-functional team with a blend of skills that encourages decentralized ownership
            through shared governance models.</p>
        <cite>—Meenagi Venkat, Vice President, Technical Sales & Solutioning for IBM Cloud</cite>
    </blockquote>
    
    <hr />
    
    <h3>Key Questions to Consider:</h3>

    <ul>
        <li>Which of your applications need major revisions to work more effectively on a cloud platform?
            <li>How can you scal9e for the episodic nature of today’s business and safeguard against cyber challenges in a multi-cloud environment?
            <li>How can you gain visibility into the health and performance of your apps, infrastructure, and data?
            <li>How can you isolate service risks and forecast issues before business is impacted?
            <li>Where will you simplify and streamline, using next-gen platforms for a continuous delivery pipeline that thrives on automated deployment and testing capabilities?
    </ul>
    
    <h3>Key Areas to Understand:</h3>
    
    <div class="padLeft keyareas">
        <p><b>Monitoring, Logging, Alerting</b></p>
        <p>It’s essential that you monitor your assets, keeping meticulous performance logs and providing rapid alerts for malfunction or low performance. What measures need have
            to be taken to ensure continuity of operations and efficient failover in times of crisis and operational disruption? How do we isolate service risks and forecast issues before business is impacted?</p>
        
        <p><b>Repeatable Processes & Assets</b></p>
        <p>Do you have consistent processes that are easily repeatable and version-controlled?<br />
            Which of your cloud assets can be managed across multiple clouds?</p>
        
        <p><b>Results: Early & Often</b></p>
        <p>You can reduce your risks by deploying frequent, incremental changes using DevOps toolchain for faster deployments.</p>
        
        <p><b>Cloud-Native Tools</b></p>
        <p>You’ll need to monitor and manage workloads with scalable, cutting-edge tools. The Cloud Native Computing Foundation shows what that landscape looks like: <a href="https://landscape.cncf.io/" target="_blank">https://landscape.cncf.io/</a></p>
