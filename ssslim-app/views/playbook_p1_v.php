    <h1>PART ONE:<br />
        Strategize & Design</h1>
    
    <h2 class="black">
        your cloud for your business needs
    </h2>
    
    <img src="<?= base_url() ?>images/playbooks/rectangle-dresses.jpg" class="rectangle" />
    
    <blockquote>
        <p>
            Good planning up front, which includes a careful definition of success and a solid
            understanding of the business needs, is critical. Having the right people with the
            right expertise involved from the beginning will reduce the risks and lower the overall
            cost of the project.
        </p>
        <cite>—Dave Krier, IBM Cloud Private Solutions Architect</cite>
    </blockquote>
    
    <hr />
    
    <h3>Key Questions to Consider:</h3>
    
    <ul>
        <li>
            What digital capabilities do you need to adopt cloud solutions that streamline
            transformation and function across multiple clouds?
        </li>
        <li>
            How can you best unlock the power of cloud solutions to increase customer and
            business value?
        </li>
        <li>
            How can you ensure that IT operates efficiently across multi-cloud environments,
            cloud services and new technologies?
        </li>
        <li>
            What changes need to be made to organizational structures in order to support
            cloud computing and as-a-service business models? How can DevOps fit help?
        </li>
    </ul>
    
    <h3>Key Areas to Understand:</h3>
    
    <div class="padLeft keyareas">
        <p><b>New Roadmap</b></p>
        <p>How have your priorities evolved? How can you respond and put new strategy into action?</p>
    
        <p><b>Automated Assessment</b></p>
        <p>Where can you automate the process of reviewing the current state of IT applications and data portfolios?</p>
    
        <p><b>Open Technology</b></p>
        <p>How can you define an open technology framework that aligns with business needs, key cloud use cases, and goals??</p>
    
        <p><b>Architecture Review</b></p>
        <p>What is the current state of your IT application, data portfolio, and legacy environments? How can they be modified or overhauled?</p>
    </div>
    
    <hr />
    
    
    <h1>PART TWO:<br />
        Move your data</h1>
    
    <h3 class="black">and applications to the cloud</h3>
    
    <img src="<?= base_url() ?>images/playbooks/rectangle-tondini.jpg" class="rectangle" />
    
    <blockquote>
        <p>Some Clients are going to want to move their legacy applications to cloud
            immediately with no change. Others will move to cloud initially, start refactoring it,
            and/or build new applications to be cloud-native.</p>
        <cite>—Mini Mehta, IBM Cloud Platform Technical Specialist</cite>
    </blockquote>
    
    <hr />
    
    <h3>Key Questions to Consider:</h3>
    
    <ul>
        <li>What are your current workloads?
        <li>Which existing applications are integral to your business moving forward? Which are already well-designed?
        <li>What next-gen applications has your company already deployed and to what degree of success?
        <li>What skilled resources are required to augment in-house functionalities and IT infrastructure?
    </ul>
    
    <h3>Key Areas to Understand:</h3>
    
    <div class="padLeft keyareas">
        <p><b>Modernizing</b></p>
        <p>What will it take to continuously modernize your IT portfolio using Cloud technologies? How can you modernize your workforce, shifting culture and operating models to optimize for cost and speed?</p>
    
        <p><b>Open Source</b></p>
        <p>Can open source technology platforms like OpenShift help optimize costs and innovation?</p>
    
        <p><b>Multi-Cloud</b></p>
        <p>How can you leverage multi-cloud components and services, including containers,
            functions, platform, and database?</p>
    
        <p><b>Culture</b></p>
        <p>How can you adopt new ways of thinking, working and collaborating? Teams will need
            to unlearn old skills and learn new skills and tools. What will be the new operating
            model to ensure that IT operates efficiently across multi-cloud environments, cloud
            services and new technologies?</p>
    </div>
    
    <hr />
    
    <h1>PART THREE:<br />
        Build and innovate</h1>
    
    <img src="<?= base_url() ?>images/playbooks/rec.jpg" class="rectangle" />
    
    <blockquote>
        <p>Experiment. Fail three out of five times. If that’s not your ratio, you’re not
            experimenting, but waiting for enough information to act. You can’t escape the
            mediocre that way.</p>
        <cite>—Temel Güzeloğlu, CEO QNB Finansbank</cite>
    </blockquote>
    
    <hr />
    
    <h3>Key Questions to Consider:</h3>
    
    <ul>
        <li>What new applications can be built with cloud-native capabilities? Which workloads are most suited for re-architecting or rebuilding, given the risks, technical challenges, and business and technology goals?
        <li>How can you enable teams for a multi-cloud reality, where skills can be applied consistently regardless of target environment?
        <li>How do you position central IT services as an enabler of speed as opposed to a gatekeeper of control, while retaining compliance and governance?
    </ul>
    
    <h3>Key Areas to Understand:</h3>
    
    <div class="padLeft keyareas">
        <p><b>DevOps</b></p>
        <p>How can you automate ways to develop, test and rapidly deploy applications? You’ll
            find a deeper look at DevOps here: <a href="https://www.ibm.com/cloud/devops" target="_blank">https://www.ibm.com/cloud/devops</a>.</p>
    
        <p><b>Cloud-Native Applications</b></p>
        <p>With IBM, building new apps starts in The Garage: an approach that enables business, development, and operations to continuously design, deliver, and validate new solutions. What new apps can you build from the ground up for speed, consistency, and reliability?</p>
    
        <p><b>Exponential Technologies</b></p>
        <p>How can you unleash the full potential of your company with next-gen technologies like AI, IoT, Automation, Blockchain, and 5G?</p>
    </div>
    
    <p><b>A Quick Overview of DevOps: The Secret Sauce</b></p>
    <p>DevOps promotes closer collaboration among line of business, development, and IT operations.<br />
        It enables business transformation through faster development of innovative software that meets emerging business needs. It’s the glue that holds everything together as you migrate to cloud.</p>
    
