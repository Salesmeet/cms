    <h2>4 Key Plays</h2>
    
    <p>Your Journey to the Cloud stretches across 4 Key Plays. The order in which companies choose to use them varies from one brand to the next. As you explore each play here, you’ll have questions that help you identify how to customize it for your needs.</p>
    
