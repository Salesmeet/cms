    <h2>
        CASE STUDY: City Furniture
    </h2>
    <h3 class="black">
        In moving SKUs and customer data to the cloud, City Furniture gave store
        associates deep knowledge of inventory and customers that they can use
        while on the floor.
    </h3>
    
    <h3>Challenge:</h3>
    
    <p>
        Given the limitations of showroom associates helping customers with antiquated “green
        screen” technology at fixed workstations, City Furniture wanted to make shopping and
        buying as informative, easy and engaging as it is online.
    </p>
    
    <div class="twocols">
        <div>
            <img class="tondino" src="<?= base_url() ?>images/playbooks/tablet.jpg" />
